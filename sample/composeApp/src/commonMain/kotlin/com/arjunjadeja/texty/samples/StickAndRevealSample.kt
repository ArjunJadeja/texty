@Composable
fun StickAndRevealSample(isDemo: Boolean) = SampleCard(
    title = "The Dark Knight Poster",
    description = "Sample demonstrating Stick and Reveal display style using ASCII art."
) {
    SampleWrappingBox(isDemo = isDemo) {
        Card(
            modifier = Modifier.size(AppDimens.sampleDemoCardSize).padding(AppDimens.paddingMedium),
            shape = RoundedCornerShape(AppDimens.cardCornerRadius),
            elevation = CardDefaults.cardElevation(defaultElevation = AppDimens.cardElevationSmall)
        ) {
            Box(
                modifier = Modifier
                    .fillMaxSize()
                    .background(Color.Black)
                    .padding(AppDimens.paddingMedium),
                contentAlignment = Alignment.Center
            ) {
                Texty(
                    text = content,
                    displayStyle = DisplayStyle.StickAndReveal(
                        cover = cover,
                        coverStickingDirection = TransitionDirection.TOP_TO_BOTTOM,
                        coverStickingDelay = 80L,
                        delayBeforeReveal = 1000L,
                        revealingDirection = TransitionDirection.BOTTOM_TO_TOP,
                        revealingDelay = 70L
                    ),
                    textStyle = TextStyle(
                        fontFamily = FontFamily.Monospace,
                        fontSize = 11.sp
                    ),
                    color = { Color.Yellow }
                )
            }
        }
    }
}

